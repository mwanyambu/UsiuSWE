    private void getRecord() {
        DefaultTableModel model =(DefaultTableModel) table.getModel();
        int n = table.getSelectedRow();
        firstname.setText((String) model.getValueAt(n, 0));
        lastname.setText((String) model.getValueAt(n, 1));
        gender.setText((String) model.getValueAt(n, 2));
        idnumber.setText((String)model.getValueAt(n, 3));
        fees.setText((String) model.getValueAt(n, 4));
    }

