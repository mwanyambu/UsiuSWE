    private void myconnection() {
        try{
            cn = DriverManager.getConnection("JDBC:MYSQL://LOCALHOST/STUDENT", "root", null);
            JOptionPane.showMessageDialog(null, "successfully connected");
            String sql = "select * from studentinfo";
            pst = cn.prepareStatement(sql, ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_UPDATABLE);
            rs = pst.executeQuery();
            
        }
        catch(Exception e){
            //System.out.println(e); //this is equivalent to to 
            e.printStackTrace();
        }
    }

    private void insertrecord() {
        try{
            pst = cn.prepareStatement("insert into studentinfo values (?,?,?,?,?)");
            pst.setString(1, firstname.getText());
            pst.setString(2, lastname.getText());
            pst.setString(3, gender.getText());
            pst.setString(4, idnumber.getText());
            pst.setString(5, fees.getText());
            
            pst.execute(); 
            //pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "record successfully submited");
            clear();
            new DemoForm().setVisible(true);
            dispose();
            
        }
        catch(Exception e){
            //System.out.println(e); //this is equivalent to to 
            e.printStackTrace();
        }
        
    }
